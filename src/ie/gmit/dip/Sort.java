package ie.gmit.dip;

/**
 * The Sort interface has one method sort which accepts an array of numbers
 * and sort its elements 
 * 
 * @author Sunoj Jose
 *
 */

public interface Sort {
	/**
	 * The implementation of sort() is different for each classes implement it.
	 * 
	 * @param numbers
	 */
	public void sort(int[] numbers);

}
